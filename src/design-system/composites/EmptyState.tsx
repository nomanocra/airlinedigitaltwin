export interface EmptyStateProps {
  /**
   * Size variant of the empty state
   * @default 'M'
   */
  size?: EmptyStateSize;
  /**
   * Built-in illustration name. Use this OR icon prop, not both.
   */
  illustration?: EmptyStateIllustration;
  /**
   * Custom icon or image to display at the top. Use this OR illustration prop, not both.
   */
  icon?: React.ReactNode;
  /**
   * Title text displayed below the icon
   */
  title: string;
  /**
   * Description text displayed below the title
   */
  description?: string;
  /**
   * Action buttons or custom content for the footer
   */
  actions?: React.ReactNode;
  /**
   * Additional class name for custom styling
   */
  className?: string;
}

/**
 * EmptyState Component
 *
 * A placeholder component displayed when content is empty or unavailable.
 * Shows an illustrative icon, title, description, and optional action buttons.
 *
 * @example
 * ```tsx
 * // Using built-in illustration
 * <EmptyState
 *   illustration="Box"
 *   title="Your Fleet is empty"
 *   description="Please add flights or import a fleet"
 *   actions={
 *     <>
 *       <Button variant="Outlined" label="ADD AIRCRAFT" leftIcon="add" />
 *       <Button variant="Default" label="IMPORT FLEET" leftIcon="download" />
 *     </>
 *   }
 * />
 *
 * // Using custom icon
 * <EmptyState
 *   icon={<Icon name="broken_image" size={64} />}
 *   title="No results"
 *   description="Try adjusting your search"
 * />
 * ```
 */
export function EmptyState({
  size = 'M',
  illustration,
  icon,
  title,
  description,
  actions,
  className,
}: EmptyStateProps) {
  const classes = [
    'empty-state',
    `empty-state--${size.toLowerCase()}`,
    className,
  ].filter(Boolean).join(' ');

  // Use illustration prop if provided, otherwise fall back to icon prop
  const displayIcon = illustration ? emptyStateIllustrations[illustration] : icon;

  return (
    <div className={classes}>
      {displayIcon && <div className="empty-state__icon">{displayIcon}</div>}

      <div className="empty-state__content">
        <h5 className="empty-state__title">{title}</h5>
        {description && (
          <p className="empty-state__description">{description}</p>
        )}
      </div>

      {actions && <div className="empty-state__actions">{actions}</div>}
    </div>
  );
}
